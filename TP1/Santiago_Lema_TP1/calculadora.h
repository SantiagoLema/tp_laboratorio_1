#ifndef CALCULADORA_H_INCLUDED
#define CALCULADORA_H_INCLUDED

#endif // CALCULADORA_H_INCLUDED

/** \brief Funcion que devuelve la suma de dos parametros.
 *
 * \param X int Primer valor a sumar.
 * \param Y int Segundo valor a sumar.
 * \return int Resultado de la suma.
 *
 */
int sumar(int X, int Y); // Funcion sumar


/** \brief Funcion que devuelve la resta de dos parametros.
 *
 * \param X int Valor del minuendo.
 * \param Y int Valor del sustraendo.
 * \return int Resultado de la resta.
 *
 */
int restar(int X, int Y); // Funcion restar


/** \brief Funcion que devuelve la division entre dos parametros.
 *
 * \param X int Valor del dividendo.
 * \param Y int Valor del divisor.
 * \return int Resultado de la division.
 *
 */
float dividir(int X, int Y); // Funcion dividir


/** \brief Funcion que devuelve la multiplicacion entre dos parametros.
 *
 * \param X int Valor del multiplicando.
 * \param Y int Valor del multiplicador.
 * \return int Resultado de la multiplicacion.
 *
 */
int multiplicar(int X, int Y); // Funcion multiplicar


/** \brief Funcion que devuelve el factorial del parametro X.
 *
 * \param X int Valor a obtener su factorial.
 * \return long long int Resultado del factorial X.
 *
 */
long long int factorialX(int X); // Funcion factorial del valor X


/** \brief Funcion que devuelve el factorial del parametro Y.
 *
 * \param Y int Valor a obtener su factorial.
 * \return long long int Resultado del factorial Y.
 *
 */
long long int factorialY(int Y); // Funcion factorial del valor X


